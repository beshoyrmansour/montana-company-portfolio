/**
 * Regenerate src/lib/world-map-paths.ts from Natural Earth 110m country borders.
 *
 *   curl -L https://raw.githubusercontent.com/nvkelso/natural-earth-vector/master/geojson/ne_110m_admin_0_countries.geojson \
 *     -o /tmp/countries.geojson
 *   node scripts/gen-world-paths.mjs
 *
 * Equirectangular projection on a 1000×500 viewBox. Integer-precision coordinates
 * keep the file under 100KB; borders look crisp up to ~1000px display width.
 */

import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import path from 'path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC = process.env.GEOJSON ?? '/tmp/countries.geojson';
const OUT = path.join(__dirname, '..', 'src', 'lib', 'world-map-paths.ts');

const W = 1000;
const H = 500;
const project = (lng, lat) => [((lng + 180) * W) / 360, ((90 - lat) * H) / 180];
const r = (n) => Math.round(n);

const geo = JSON.parse(readFileSync(SRC, 'utf8'));

const pathFor = (geometry) => {
  const polys = geometry.type === 'Polygon' ? [geometry.coordinates] : geometry.coordinates;
  const parts = [];
  for (const poly of polys) {
    for (const ring of poly) {
      const pts = ring.map(([lng, lat]) => project(lng, lat));
      if (pts.length < 3) continue;
      const uniq = [pts[0]];
      for (let i = 1; i < pts.length; i++) {
        const p = pts[i];
        const q = uniq[uniq.length - 1];
        if (r(p[0]) !== r(q[0]) || r(p[1]) !== r(q[1])) uniq.push(p);
      }
      if (uniq.length < 3) continue;
      let d = `M${r(uniq[0][0])} ${r(uniq[0][1])}`;
      for (let i = 1; i < uniq.length; i++) d += `L${r(uniq[i][0])} ${r(uniq[i][1])}`;
      d += 'Z';
      parts.push(d);
    }
  }
  return parts.join('');
};

const out = [];
for (const f of geo.features) {
  const iso = f.properties.ISO_A2_EH || f.properties.ISO_A2;
  if (!iso || iso === '-99') continue;
  const d = pathFor(f.geometry);
  if (d) out.push({ iso, d });
}

const ts = `// Generated by scripts/gen-world-paths.mjs from Natural Earth 110m admin_0_countries.
// Equirectangular projection, 1000×500 viewBox, integer-precision coordinates.
// Do not hand-edit — regenerate if you need fresher borders.

export const WORLD_MAP_VIEWBOX = '0 0 ${W} ${H}';
export const WORLD_MAP_W = ${W};
export const WORLD_MAP_H = ${H};

export interface CountryPath {
  iso: string;
  d: string;
}

export const WORLD_MAP_PATHS: ReadonlyArray<CountryPath> = ${JSON.stringify(out)};
`;

writeFileSync(OUT, ts);
console.log(`Wrote ${out.length} country paths, ${ts.length} bytes → ${path.relative(process.cwd(), OUT)}`);
